        public static byte get_key_no_cycles(ref long seed, short tap_pos, short seed_size,int iterations)
        {
            byte ret = 0;    //ban7ot feha el value ba3d ma3mlha xor, return el value
            for (int i = 0; i < iterations; i++) //el rakem el wahed 8 bit 
            {
                byte xor = 0;
                if ((seed & (1 << tap_pos)) > 0) xor = 1; // bn3malha AND ma3 msk tany kolel seed ba zero wel index ely 3ezanha ba 1, 3ala el tap
                if ((seed & (1 << (seed_size - 1))) > 0) xor = (byte)(xor ^ 1); // 3ala akher element
                seed &= msk;
                seed <<= 1;
                seed += xor;
                ret <<= 1;
                ret += xor;
            }
            return ret;
        }
        public static byte get_key_with_cycles(ref long seed, short tap_pos, short seed_size, int iterations)
        {
            long tmppp = seed;
            if (dp.ContainsKey(seed) == true)
            {
                seed = dp[seed].Key;
                return dp[seed].Value;
            }
            byte ret = 0;
            for (int i = 0; i < iterations; i++)
            {
                byte xor = 0;
                if ((seed & (1 << tap_pos)) > 0) xor = 1;
                if ((seed & (1 << (seed_size - 1))) > 0) xor = (byte)(xor ^ 1);
                seed &= msk;
                seed <<= 1;
                seed += xor;
                ret <<= 1;
                ret += xor;
            }
            dp.Add(tmppp, new KeyValuePair<long, byte>(seed, ret));
            seed = dp[tmppp].Key;
            return dp[tmppp].Value;
        }
        public static RGBPixel[,] Encrypt_No_cycles(RGBPixel[,] ImageMatrix, short seed_size, short tap_pos, long seed)
        {
            RGBPixel[,] ret = new RGBPixel[ImageMatrix.GetLength(0), ImageMatrix.GetLength(1)];
            for (int i = 0; i < seed_size - 1; i++)
                msk |= (1 << i);
            int N = ImageMatrix.GetLength(0);
            int M = ImageMatrix.GetLength(1);
            int j = 0;
            for (int i = 0; i < N; i++)
            {
                for (j = 0; j < M; j++)
                {
                    ret[i, j].red = ImageMatrix[i, j].red;
                    ret[i, j].red ^= get_key_no_cycles(ref seed, tap_pos, seed_size, 8);
                    ret[i, j].green = ImageMatrix[i, j].green;
                    ret[i, j].green ^= get_key_no_cycles(ref seed, tap_pos, seed_size, 8);
                    ret[i, j].blue = ImageMatrix[i, j].blue;
                    ret[i, j].blue ^= get_key_no_cycles(ref seed, tap_pos, seed_size, 8);
                }
            }
            return ret;
        }
        /// <summary>
        /// Open an image and load it into 2D array of colors (size: Height x Width)
        /// </summary>
        /// <param name="ImagePath">Image file path</param>
        /// <returns>2D array of colors</returns>
        public static RGBPixel[,] OpenImage(string ImagePath)
        {
            Bitmap original_bm = new Bitmap(ImagePath);
            int Height = original_bm.Height;
            int Width = original_bm.Width;

            RGBPixel[,] Buffer = new RGBPixel[Height, Width];

            unsafe
            {
                BitmapData bmd = original_bm.LockBits(new Rectangle(0, 0, Width, Height), ImageLockMode.ReadWrite, original_bm.PixelFormat);
                int x, y;
                int nWidth = 0;
                bool Format32 = false;
                bool Format24 = false;
                bool Format8 = false;

                if (original_bm.PixelFormat == PixelFormat.Format24bppRgb)
                {
                    Format24 = true;
                    nWidth = Width * 3;
                }
                else if (original_bm.PixelFormat == PixelFormat.Format32bppArgb || original_bm.PixelFormat == PixelFormat.Format32bppRgb || original_bm.PixelFormat == PixelFormat.Format32bppPArgb)
                {
                    Format32 = true;
                    nWidth = Width * 4;
                }
                else if (original_bm.PixelFormat == PixelFormat.Format8bppIndexed)
                {
                    Format8 = true;
                    nWidth = Width;
                }
                int nOffset = bmd.Stride - nWidth;
                byte* p = (byte*)bmd.Scan0;
                for (y = 0; y < Height; y++)
                {
                    for (x = 0; x < Width; x++)
                    {
                        if (Format8)
                        {
                            Buffer[y, x].red = Buffer[y, x].green = Buffer[y, x].blue = p[0];
                            p++;
                        }
                        else
                        {
                            Buffer[y, x].red = p[2];
                            Buffer[y, x].green = p[1];
                            Buffer[y, x].blue = p[0];
                            if (Format24) p += 3;
                            else if (Format32) p += 4;
                        }
                    }
                    p += nOffset;
                }
                original_bm.UnlockBits(bmd);
            }

            return Buffer;
        }
        
        /// <summary>
